export function mergeOrderSheetInventory(inventory: InventoryItem[]) {
  const importedNames = new Set(orderSheetInventory.map((item) => item.itemName.toLowerCase()));
  const preserved = inventory.filter((item) => !importedNames.has(item.itemName.toLowerCase()));
  const emptyImportedInventory = orderSheetInventory.map((item) => ({
    ...item,
    currentAmount: 0,
  }));

  return [...emptyImportedInventory, ...preserved];
}
